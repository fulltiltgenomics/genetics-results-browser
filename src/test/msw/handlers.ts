import { http, HttpResponse } from "msw";

// fixtures captured from the live genetics-results-api (see fixtures/README.md)
import credibleSetsByVariant from "../fixtures/credible_sets_by_variant.json";
import credibleSetsByVariantBatch from "../fixtures/credible_sets_by_variant_batch.json";
import credibleSetsByGene from "../fixtures/credible_sets_by_gene.json";
import credibleSetsByQtlGene from "../fixtures/credible_sets_by_qtl_gene.json";
import genesInRegion from "../fixtures/genes_in_region.json";
import searchGenes from "../fixtures/search_genes.json";
import variantAnnotationFinngen from "../fixtures/variant_annotation_finngen.json";
import nearestGenes from "../fixtures/nearest_genes.json";
import datasets from "../fixtures/datasets.json";
import resources from "../fixtures/resources.json";
import searchPhenotypes from "../fixtures/search_phenotypes.json";
import summaryStats from "../fixtures/summary_stats.json";
import colocalizationByCsId from "../fixtures/colocalization_by_credible_set_id.json";
import traitNameMapping from "../fixtures/trait_name_mapping.json";
// gene-evidence tab fixtures (refactor.md §6). gene_based is served as TSV, so import it raw.
import geneBasedTsv from "../fixtures/gene_based.tsv?raw";
import expressionByGene from "../fixtures/expression_by_gene.json";
import geneDisease from "../fixtures/gene_disease.json";
// BFF stage-1 aggregation fixture (NormalizedResponse), generated by running bff/normalize over the
// granular fixtures below; mocks POST /v1/results which the BFF owns (not an upstream API endpoint).
import normalizedResponse from "../fixtures/normalized_response.json";

// VITE_API_URL differs per env ("/api", "http://localhost:2000/api", ":4000/api"), so match on
// the path suffix with a leading wildcard — works whatever base the axios client is configured with.
const api = (suffix: string): string => `*/api/v1/${suffix}`;

export const handlers = [
  // the BFF stage-1 endpoint the frontend's useNormalizedQuery hits; the granular handlers below mock
  // the upstream calls the BFF itself fans out to (used by the bff-side tests, not the browser hook).
  http.post(api("results"), () => HttpResponse.json(normalizedResponse)),

  http.get(api("credible_sets_by_variant/:variant"), () => HttpResponse.json(credibleSetsByVariant)),
  http.post(api("credible_sets_by_variant"), () => HttpResponse.json(credibleSetsByVariantBatch)),

  http.get(api("credible_sets_by_gene/:gene"), () => HttpResponse.json(credibleSetsByGene)),
  http.get(api("credible_sets_by_qtl_gene/:gene"), () =>
    HttpResponse.json(credibleSetsByQtlGene)
  ),

  http.get(api("genes_in_region/:chr/:start/:end"), () => HttpResponse.json(genesInRegion)),

  http.get(api("variant_annotation/:source"), () => HttpResponse.json(variantAnnotationFinngen)),
  http.post(api("variant_annotation/:source"), () => HttpResponse.json(variantAnnotationFinngen)),

  http.post(api("nearest_genes"), () => HttpResponse.json(nearestGenes)),

  http.get(api("datasets"), () => HttpResponse.json(datasets)),
  http.get(api("resources"), () => HttpResponse.json(resources)),

  // /search is shared by phenotype autocomplete and gene-coordinate lookup; branch on types=
  http.get(api("search"), ({ request }) => {
    const params = new URL(request.url).searchParams;
    if (params.get("types") === "genes") {
      return HttpResponse.json(searchGenes);
    }
    // mirror the API's has_summary_stats / has_credible_sets gates so the tab vs main search
    // behaviors are exercised (tab requires sumstats; main requires credible sets)
    let phenos = searchPhenotypes as Array<{
      has_summary_stats?: boolean;
      has_credible_sets?: boolean;
    }>;
    if (params.get("has_summary_stats") === "true") {
      phenos = phenos.filter((p) => p.has_summary_stats);
    }
    if (params.get("has_credible_sets") === "true") {
      phenos = phenos.filter((p) => p.has_credible_sets);
    }
    return HttpResponse.json(phenos);
  }),

  http.get(api("summary_stats/:resource/:dataType"), () => HttpResponse.json(summaryStats)),

  http.get(api("colocalization_by_credible_set_id/:resource/:phenotype/:csId"), () =>
    HttpResponse.json(colocalizationByCsId)
  ),

  // variant-anchored coloc used by ColocSection (works for all cs-id formats, unlike the cs-id route)
  http.get(api("colocalization_by_variant/:variant/:resource/:phenotype"), () =>
    HttpResponse.json(colocalizationByCsId)
  ),

  // {phenocode: phenostring} map used to resolve GWAS coloc partner names (ColocSection)
  http.get(api("trait_name_mapping"), () => HttpResponse.json(traitNameMapping)),

  // peak->gene links used lazily by the caQTL tissue summary "linked genes" column (usePeakGenes).
  // two rows for one peak across different link cell types -> the hook unions to a single symbol.
  http.get(api("peak_to_genes/:peak"), ({ params }) =>
    HttpResponse.json([
      { peak_id: String(params.peak), gene_id: "ENSG00000130203", symbol: "APOE", cell_type: "l1.B" },
      { peak_id: String(params.peak), gene_id: "ENSG00000130203", symbol: "APOE", cell_type: "l1.Mono" },
    ])
  ),

  // harmonized per-phenotype metadata (counts) used lazily by PhenotypeTooltip on hover
  http.get(api("resource_metadata/:resource"), () =>
    HttpResponse.json([
      {
        phenotype_code: "G6_ALZHEIMER",
        phenotype_string: "Alzheimer's disease",
        n_samples: 412181,
        n_cases: 9301,
        n_controls: 402880,
        trait_type: "binary",
      },
    ])
  ),

  // gene-evidence tab: gene_based is TSV (text), the other two are JSON
  http.get(api("gene_based/:gene"), () =>
    HttpResponse.text(geneBasedTsv, { headers: { "Content-Type": "text/tab-separated-values" } })
  ),
  http.get(api("expression_by_gene/:gene"), () => HttpResponse.json(expressionByGene)),
  http.get(api("gene_disease/:gene"), () => HttpResponse.json(geneDisease)),
];
